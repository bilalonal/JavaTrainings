package thenewbostonAndBook;

public class Switch {
	public static void main(String[]args){
		int age = 5;
		
		switch(age){
			case 1:
				System.out.println("noob");
				break;
			case 2:
				System.out.println("beginner");
				break;
			case 3:
				System.out.println("advanced");
				break;
			default:
				System.out.println("expert");
				break;
		}
		
		
		
	
	
	
	
	
	
	
	
	
	
	
}
}
